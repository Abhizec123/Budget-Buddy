    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
 
        int rs=table1.getSelectedRow();
        if(rs!=-1)
        {
            int r=JOptionPane.showConfirmDialog
        (null, "Do you really want to delete?", "Confirmation", JOptionPane.YES_NO_OPTION);
        
            if(r==JOptionPane.YES_OPTION)
            {
                String cat=(String)table1.getValueAt(rs, 1);

                try
                {
                    Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/expensedb","root","Tmsl@123");   

                    Statement s=con.createStatement();
                    s.executeUpdate("delete from category_info where category='"+cat+"' ");
                    JOptionPane.showMessageDialog(this,"Category Deleted Successfully!");
                    getEntries();//refreshing table

                    con.close();
                }
                catch(Exception ex)
                {
                    JOptionPane.showMessageDialog(this,ex);
                } 
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Please Select a Row");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    
