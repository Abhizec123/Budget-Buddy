    void getEntries()
    {
        try
        {
            javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel)table1.getModel();
            int rc=dtm.getRowCount();
            while(rc--!=0)
            {
                dtm.removeRow(0);
            }
            
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/expensedb","root","Tmsl@123");   
           
            Statement s=con.createStatement();
            ResultSet rs=s.executeQuery("select* from category_info");
            
            int sno=0;
            while(rs.next())
            {
                String cat=rs.getString("category");
                
                Object o[]={++sno,cat};
                dtm.addRow(o);
            }
        
            con.close();
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this,ex);
        }
    }

