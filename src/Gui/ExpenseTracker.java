    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        try 
        {
            java.util.Date dt=date.getDate();
            String a=amt.getText();
            String c=(String)combo1.getSelectedItem();
            
            if(dt!=null && !a.equals("") && !c.equals(""))
            {
                int amnt=Integer.parseInt(a);
                //converting java.util date to sql date
                java.sql.Date date =new java.sql.Date(dt.getTime());
                //Single code not needed for amnt since not varchar
                db.DbConnect.s.executeUpdate
        ("insert into spendings (category,sdate,amount) values ('"+c+"','"+date+"',"+amnt+")"); 
                JOptionPane.showMessageDialog(this, "Expense Added Successfully!");
                getEntries();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Please Fill all the Details");
            }
        } 
        catch (Exception ex) 
        {
            JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        int rs=table2.getSelectedRow();
        if(rs!=-1)
        {
            int id=(int)table2.getValueAt(rs, 0);
            
            int r=JOptionPane.showConfirmDialog
       (null, "Do you really want to delete?", "Confirmation", JOptionPane.YES_NO_OPTION);

            if(r==JOptionPane.YES_OPTION)
            {
                try 
                {
                    db.DbConnect.s.executeUpdate("delete from spendings where sid='"+id+"' ");
                    JOptionPane.showMessageDialog(this,"Record Deleted Successfully!");
                    getEntries();
                } 
                catch (Exception ex)
                {
                    JOptionPane.showMessageDialog(this,ex);
                }
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Please Select a Row");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

