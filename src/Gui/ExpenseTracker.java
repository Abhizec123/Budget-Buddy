    private void displayCategory()
    {
        try 
        {
            combo1.removeAllItems();
            ResultSet rs=db.DbConnect.s.executeQuery("select * from category_info");
            while(rs.next())
            {
                combo1.addItem(rs.getString("category"));
            }
        }
        catch (Exception ex) 
        {
            JOptionPane.showMessageDialog(this,ex);
        }
    }
    private void getEntries()
    {
        try 
        {
            //To update as soon as new data is added
            javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel)table2.getModel();
            int rc=dtm.getRowCount();
            while(rc--!=0)
            {
                dtm.removeRow(0);
            }
            
            java.time.LocalDate cd=java.time.LocalDate.now();
            java.time.LocalDate bd=cd.minusDays(30);
            ResultSet rs=db.DbConnect.s.executeQuery
            ("select * from spendings where sdate<='"+cd+"' and sdate>='"+bd+"' order by sdate asc");
            
            int total=0;
            while(rs.next())
            {            
                int t=rs.getInt("amount");
                total+=t;
                Object o[]={rs.getInt("sid"),rs.getDate("sdate"),rs.getString("category"),t};
                dtm.addRow(o);
            }
            sp.setText(Integer.toString(total));
        }
        catch (Exception ex) 
        {
            JOptionPane.showMessageDialog(this,ex);
        }
    }

